    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(mdp.getPassword().equals("") || nom.getText().equals("") || prenom.getText().equals("") || user.getText().equals("")){
            ButtonInfo bi = new ButtonInfo(new JFrame(),true);
                bi.pan.setBackground(Color.red);
                bi.titre1.setText("Erreur");
                bi.titre2.setText("Tous les champs doivent être remplis");
                bi.titre3.setText("");
                bi.setVisible(true);
        }else{
            //tests
            char[] c = mdp.getPassword();
            String mdpV = new String(c);
            if(herPass.equals(mdpV)){
                //faire d'autres tests
                char[] c2 = n_mdp.getPassword(),c3 = n_mdp2.getPassword();
                String n_mdpV = new String(c2);
                String n_mdpV2 = new String(c3);
                if(n_mdpV.equals(n_mdpV2)){
                    //traitements finaux
                    Admin ad = new Admin(user.getText(),nom.getText(),prenom.getText(),n_mdpV);
                    ad.Modifier();
                    this.dispose();
                    al.InitAdmin();
                }else{
                   ButtonInfo bi = new ButtonInfo(new JFrame(),true);
                    bi.pan.setBackground(Color.red);
                    bi.titre1.setText("Erreur");
                    bi.titre2.setText("Les deux mot de passe ne correspondent Pas");
                    bi.titre3.setText("Vérifiez les deux mots de passe");
                    bi.setVisible(true); 
                }
            }else{
                ButtonInfo bi = new ButtonInfo(new JFrame(),true);
                bi.pan.setBackground(Color.red);
                bi.titre1.setText("Erreur");
                bi.titre2.setText("Le mot de passe n'est pas valide");
                bi.titre3.setText("Vous avez entré un mot de passe différent de celui de cet admin");
                bi.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

