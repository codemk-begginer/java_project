    public void IniAdmin(){
        BD bd = new BD();
        Connection conn = bd.GetConnection();
        try {
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery("SELECT * FROM admin WHERE user = \""+userV+"\"");
            rs.first();
            titre.setText("Modification de L'admin " +rs.getString("nom")+" "+rs.getString("prenom"));
            nom.setText(rs.getString("nom"));prenom.setText(rs.getString("prenom"));user.setText(rs.getString("user"));
            herPass = rs.getString("mdp");
        } catch (SQLException ex) {
            Logger.getLogger(AdminEdit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
